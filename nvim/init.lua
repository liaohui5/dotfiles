require("options")
require("mappings")
require("plugins")
require("autocmd")
require("plugin-configs.index")
require("lsp-configs.index")

--[[
关于配置:
  1. 所有初始化的 autocmd 都在 /lua/autocmd.lua 中
  2. 所有配置选项都在 /lua/options.lua 中
  3. 插件管理器用的是 Packer 注意需要先安装 Packer, 然后再安装其他插件, 具体看 lua/plugins.lua
  4. 所有的插件的配置文件都在 /plugin/*.lua 中, 因为这个目录下的 lua 会自动加载
  5. 所有快捷键在 lua/mappings, 所有插件的快捷键在 keybindings 中

关于代码提示和自动完成
  1. 如果使用 coc.nvim : coc.nvim 的配置文件时 plugin/coc.lua & coc.vim
  2. 如果使用 lsp + cmp: lsp_settings 目录存放的是对应编程语言的 lsp server 的配置, 总的配置在 plugin/lsp.lua
]]
